function getStartTimeName(timeStr) {
  let ret = timeStr
  startTimeList.forEach(item => {
    if (item.value === timeStr) {
      ret = item.name
    }
  })
  return ret
}

function getEndTimeName(timeStr) {
  let ret = timeStr
  endTimeList.forEach(item => {
    if (item.value === timeStr) {
      ret = item.name
    }
  })
  return ret
}

export function timeRange(period) {
  const startTime = getStartTimeName(period.start_time)
  const endTime = getEndTimeName(period.end_time)
  return `${startTime}-${endTime}`
}
